<?php

namespace App\Http\Requests;

use Illuminate\Foundation\Http\FormRequest;

class RejectServiceRequestRequest extends FormRequest
{
    public function authorize(): bool
    {
        return true;
    }

    public function rules(): array
    {
        return [
            'rejection_reason' => 'required|string|min:10|max:500',
        ];
    }

    public function messages(): array
    {
        return [
            'rejection_reason.required' => 'La razón del rechazo es obligatoria.',
            'rejection_reason.min' => 'La razón debe tener al menos 10 caracteres.',
            'rejection_reason.max' => 'La razón no debe exceder los 500 caracteres.',
        ];
    }
}
